public class SwitchCase {
    public static void main(String[] args) {
        int month = 4;
        switch (month) {
            case 1:
                System.out.println("1월 입니다.");
                break;
            case 2:
                System.out.println("2월 입니다.");
                break;
            case 3:
                System.out.println("3월 입니다.");
                break;
            default:
                System.out.println("몰라요 나도");
        }
        System.out.println("끝");
    }
}
